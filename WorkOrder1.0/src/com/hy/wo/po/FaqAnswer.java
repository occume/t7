package com.hy.wo.po;

import java.util.Date;

public class FaqAnswer {
	
	private int id;
	
	private String guy;
	private String desc;
	private Date cdate;
	
	public FaqAnswer(FaqRep fr) {
		this.guy = fr.getGuy();
		this.desc = fr.getDesc();
		this.cdate = fr.getCdate();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGuy() {
		return guy;
	}
	public void setGuy(String guy) {
		this.guy = guy;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	public Date getCdate() {
		return cdate;
	}
	public void setCdate(Date cdate) {
		this.cdate = cdate;
	}
	
	
}
