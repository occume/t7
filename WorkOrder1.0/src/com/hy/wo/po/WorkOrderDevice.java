package com.hy.wo.po;
/**
 * 工单硬件信息
 * @author dong_jin
 *
 */
public class WorkOrderDevice {
	private long id;
	private WorkOrder workOrder;// 所属工单
	private String cpu;//cpu信息
	private String memory;//内存信息
	private String displaycard;//显卡信息
	private String os;//操作系统
	private String netinfo;//网络商
	
	public WorkOrder getWorkOrder() {
		return workOrder;
	}
	public void setWorkOrder(WorkOrder workOrder) {
		this.workOrder = workOrder;
	}
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getCpu() {
		return cpu;
	}
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}
	public String getMemory() {
		return memory;
	}
	public void setMemory(String memory) {
		this.memory = memory;
	}
	public String getDisplaycard() {
		return displaycard;
	}
	public void setDisplaycard(String displaycard) {
		this.displaycard = displaycard;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getNetinfo() {
		return netinfo;
	}
	public void setNetinfo(String netinfo) {
		this.netinfo = netinfo;
	}
	
}
