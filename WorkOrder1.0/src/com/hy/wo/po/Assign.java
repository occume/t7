package com.hy.wo.po;
/**
 * 派单操作记录类
 * @author dong_jin
 *
 */
public class Assign {
	private String statu;
	private String from;
	private String to;
	public String getStatu() {
		return statu;
	}
	public void setStatu(String statu) {
		this.statu = statu;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	
}
